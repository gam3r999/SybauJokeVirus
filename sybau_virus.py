def play_byebyte():
    """Play byebyte.wav (or generated fallback) on a separate channel, looping."""
    global _byebyte_sound, _byebyte_channel
    if not PYGAME_OK:
        return
    try:
        if BYEBYTE_FILE:
            snd = pygame.mixer.Sound(BYEBYTE_FILE)
            print(f"[*] Byebyte: {os.path.basename(BYEBYTE_FILE)}")
        else:
            print("[!] byebyte.wav not found — using generated bytebeat fallback")
            snd = pygame.mixer.Sound(buffer=_gen_bytebeat(3.0))
        snd.set_volume(1.0)
        ch = pygame.mixer.find_channel(True)
        if ch:
            ch.play(snd, loops=-1)
            _byebyte_channel = ch
            _byebyte_sound   = snd
        else:
            print("[!] No free mixer channel for byebyte")
    except Exception as e:
        print(f"[!] Byebyte error: {e}")

