SAMPLE_RATE = 8000

def _bytebeat2(t):
    return (t * ((t >> 12 | t >> 8) & 63)) & 0xFF

def _gen_bytebeat(duration_sec=3.0):
    n    = int(SAMPLE_RATE * duration_sec)
    data = bytes([_bytebeat2(t) & 0xFF for t in range(n)])
    buf  = io.BytesIO()
    with wave.open(buf, 'w') as wf:
        wf.setnchannels(1)
        wf.setsampwidth(1)
        wf.setframerate(SAMPLE_RATE)
        wf.writeframes(data)
    buf.seek(0)
    return buf.read()

